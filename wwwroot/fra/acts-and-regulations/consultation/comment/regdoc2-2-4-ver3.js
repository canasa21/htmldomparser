<ul>
<li>une liste de v&eacute;rification pour l&rsquo;examen
pr&eacute;liminaire &agrave; l&rsquo;&eacute;valuation de l&rsquo;aptitude au travail destin&eacute;e aux superviseurs </li>
<li>une liste de v&eacute;rification pour l&rsquo;examen
pr&eacute;liminaire &agrave; l&rsquo;auto-&eacute;valuation de l&rsquo;aptitude au travail destin&eacute;e aux
travailleurs</li>
<li>des appareils de d&eacute;tection passive de l&rsquo;alcool </li>
<li>des chiens et des appareils de d&eacute;tection des
drogues (par exemple des d&eacute;tecteurs ioniques)</li>
<li>des fouilles physiques</li>
</ul>
