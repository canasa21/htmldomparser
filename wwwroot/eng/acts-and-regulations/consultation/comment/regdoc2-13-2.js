<p>Any  voluntary disclosure of non-compliance should be accompanied by a cover letter,  signed by a senior company officer, which clearly states that its purpose is to  disclose non-compliance with the <em>Nuclear  Safety and Control Act</em>, <em>Nuclear  Non-proliferation Import and Export Control Regulations</em>, or import and export licences. The letter  should be addressed to:</p>
